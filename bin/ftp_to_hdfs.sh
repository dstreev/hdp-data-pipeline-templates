#!/bin/bash

#######
# Get file from an FTP site and put it on HDFS.
#
# Feed file format:
#   FTP_SOURCE_DIR FILE_PREFIX HDFS_TARGET_DIR
#   /Data/Outbound/Curves USD_TREASURY_ /user/wre/wrelib/curves
#######

DATE_WITHOUT_DASHES=false

while [ $# -gt 0 ]; do
  case "$1" in
    --env)
      shift
      ENV=$1
      shift
      ;;
    --ftp_env)
      shift
      FTP_ENV=$1
      shift
      ;;
    --feed_file)
      shift
      FEED_FILE=$1
      shift
      ;;
    --date)
      shift
      DT=$1
      shift
      ;;
    --date_without_dashes)
        shift
        DATE_WITHOUT_DASHES=true
        ;;
    --check)
      CHECK="yes"
      shift
      ;;
    --help)
      echo "Usage: $0 --date <date in yyyyMMdd> --help"
      exit -1
      ;;
    *)
      break
      ;;
  esac
done

if [ "$DT" == "" ]; then
    DT=`date +%Y%m%d`
fi

if [ -f ${FTP_ENV} ]; then
    . ${FTP_ENV}
else
    echo "Can't locate environment file ${FTP_ENV}"
    exit -1
fi

if [ "${DATE_WITHOUT_DASHES}" == "true" ]; then
    GOOD_DT="${DT}"
else
    GOOD_DT="${DT:0:4}-${DT:4:2}-${DT:6:2}"
fi

# Create a temp directory
BASE_OUTPUT_DIR=$( mktemp -d /tmp/transfer_XXXXXXX )

exec< $FEED_FILE

while read line ; do
    ar=( $line )

    # Skip Comment lines
    s1=$line
    s2=#

    # Skip Commented lines
    if [ "${s1:0:${#s2}}" != "$s2" ]; then

        SOURCE_DIR="${ar[0]}"
        PREFIX="${ar[1]}"
        TARGET_DIR="${ar[2]}"

        # Fetch Files generated by Quantlib from the FTP Site.
        FULL_FILE="${PREFIX}${GOOD_DT}.csv"

        echo "Getting  ${FULL_FILE} files from ftp"

ftp -in <<EOF
        open $FTP_HOST
        user $FTP_USER $FTP_PASSWORD
        cd $SOURCE_DIR
        lcd ${BASE_OUTPUT_DIR}
        mget ${FULL_FILE}
        quit
EOF

        echo "Remove old versions of $TARGET_DIR/$FULL_FILE"
        hdfs dfs -rm $TARGET_DIR/$FULL_FILE
        echo "Push files ${FULL_FILE} to HDFS Directory: ${TARGET_DIR}"
        hdfs dfs -put ${BASE_OUTPUT_DIR}/$FULL_FILE $TARGET_DIR

    fi
done

